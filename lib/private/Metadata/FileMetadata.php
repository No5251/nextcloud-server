<?php

namespace OC\Metadata;

use OCP\AppFramework\Db\Entity;
use JsonSerializable;

/**
 * @method int getFileId()
 * @method void setFileId(int $fileId)
 * @method string getGroupName()
 * @method void setGroupName(string $groupName)
 */
class FileMetadata extends Entity {
	protected int $fileId;
	protected string $groupName;
	protected string $data;
	private array $metadata;

	public function __construct() {
		$this->addType('groupName', 'string');
		$this->addType('data', 'string');
	}

	public function columnToProperty($column) {
		if ($column === 'id') {
			return 'fileId';
		} else {
			return parent::columnToProperty($column);
		}
	}

	public function propertyToColumn($property) {
		if ($property === 'fileId') {
			return 'id';
		} else {
			return parent::propertyToColumn($property);
		}
	}

	public function getMetadata(): array {
		return $this->metadata;
	}

	public function setMetadata(array $data): void {
		$this->metadata = $data;
		$this->setter('data', [json_encode($data), $data]);
	}

	protected function setter($name, $args) {
		if ($name === 'data') {
			$this->metadata = $args[1] ?? json_decode($args[0]);
		}
		parent::setter($name, $args); // TODO: Change the autogenerated stub
	}
}
